package J003_statements;

public class S004_Switchcatch {
	public static void main(String[] args) {

		int choice = 5;

		switch (choice) {
		case 1:
			System.out.println("English");
			break;
		case 2:
			System.out.println("Hindi");
			break;
		case 3:
			System.out.println("Gujarati");
			break;
		default : 
			System.out.println("Invalid choice !!!");
			
		}

	}
}
