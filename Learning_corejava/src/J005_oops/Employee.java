package J005_oops;

public class Employee {
	
	private int id;
	private String name;
	private double sal;
	
	public double getSal() {
		return sal;
	}

	public void setSal(double sal) {
		this.sal = sal;
	}

	public void setId(int id)
	{
		this.id  =id;
	}
	
	public int getId()
	{
		return id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
	
}
